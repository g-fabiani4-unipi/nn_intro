<section class="prose">
	<h2>Key Takeaways</h2>
	<ul>
		<li>
			The perceptron is a <strong>supervised classifier</strong> vaguely inspired
			by how neurons operate in the human brain;
		</li>
		<li>
			The unit of a perceptron operates by performing a <strong
				>weighted sum</strong
			>
			of its inputs and passing the result through an
			<strong>activation function</strong>;
		</li>
		<li>
			<strong>Perceptron parameters are automatically</strong> updated by comparing
			the desired output with the actual output of the perceptron and applying appropriate
			corrections in a process called &ldquo;training&rdquo;;
		</li>
		<li>
			<strong>Single-layer perceptrons</strong> can only solve
			<strong>linearly separable</strong> classification tasks;
		</li>
		<li>
			In <strong>multi-layer perceptrons</strong> the
			<strong>hidden layers</strong> project the input data into a space where the
			problem is linearly separable.
		</li>
	</ul>
</section>

